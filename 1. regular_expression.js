//編寫一個正則表達式, 用於驗證密碼複雜度, 密碼要求: 8-16位, 必須包含大小寫字母, 數字, 特殊字符 並且不能有空格 例如: w34fery65@#$%^
//string 並且遞迴boolen值

function checkPassword(password) {
    var reg = /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[!@#$%^&*])[A-Za-z\d!@#$%^&*]{8,16}$/;
    return reg.test(password);
}
