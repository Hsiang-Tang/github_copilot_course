// 編寫一個正則表達式, 用於驗證IP地址, 並且可以提取IP地址中的各個string 並且遞迴boolen值
// 地址範圍要求: 192.168.10.1 到 192.168.100.255

function checkIP(ip) {
    var reg = /^192\.168\.(1[0-9][0-9]|100)\.(25[0-5]|2[0-4][0-9]|1[0-9][0-9]|[1-9][0-9]|[0-9])$/;
    return reg.test(ip);
}



